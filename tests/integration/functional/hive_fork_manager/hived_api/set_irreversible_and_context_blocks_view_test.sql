CREATE FUNCTION test_then()
    RETURNS void
    LANGUAGE 'plpgsql'
STABLE
AS
$BODY$
BEGIN
    -- bot block 8 and 9 must be in fork 2 version, sine application may still process block 9 from fork 2
    ASSERT ( SELECT COUNT(*) FROM hive.context_blocks_view WHERE num = 8 OR num = 9 ) = 2, 'No expected block nr. 8 in the hive.context_blocks_view';
    ASSERT NOT EXISTS (
        SELECT * FROM hive.context_blocks_view WHERE num = 8 OR num = 9
        EXCEPT SELECT * FROM ( VALUES
                  (8, '\xBADD8002'::bytea, '\xCAFE80'::bytea, '2016-06-22 19:10:28-07'::timestamp, 5, '\x4007'::bytea, '[]'::jsonb, '\x2157'::bytea, 'STM65w')
